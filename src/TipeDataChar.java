public class TipeDataChar {
    public static void main(String[] args) {

        // penulisan data char menggunakan petik satu ( ' )
        // hanya dapat diisi dengan satu karakter
        char b = 'B';
        char r = 'R';
        char e = 'E';
        char t = 'T';

        // print tidak akan membuat output line baru
        System.out.print(b);
        System.out.print(r);
        System.out.print(e);
        System.out.println(t);

        // println akan membuat output line baru
        System.out.println(b);
        System.out.println(r);
        System.out.println(e);
        System.out.println(t);
    }
}



