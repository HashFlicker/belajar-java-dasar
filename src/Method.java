public class Method {
    public static void main(String[] args) {
        /*
            Method adalah block kode program yang akan berjalan ketika dipanggil

            Method dibuat di bawah method Main
         */

        sayHelloWorld();
        sayHelloWorld();
        sayHelloWorld();
    }

    static void sayHelloWorld(){
        System.out.println("Hello World");
        System.out.println("Halo Dunia");
        System.out.println("Halo Jingan");
    }
}
