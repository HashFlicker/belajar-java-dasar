public class OperasiPerbandingan {
    public static void main(String[] args) {
        /*
            Operator        Keterangan
            <               Kurang Dari
            >               Lebih Dari
            <=              Kurang Dari Sama Dengan
            >=              Lebih Dari Sama Dengan
            ==              Sama Dengan
            !=              Tidak Sama Dengan
         */

        int a = 100;
        int b = 100;

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a <= b);
        System.out.println(a >= b);
        System.out.println(a == b);
        System.out.println(a != b);
    }
}
