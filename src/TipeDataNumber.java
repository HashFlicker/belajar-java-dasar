public class TipeDataNumber {

    public static void main(String[] args) {

        // Integer Number
        byte iniByte = 100;
        short iniShort = 1000;
        int iniInt = 1000000;
        long iniLong = 1000000000;
        long iniLong2 = 10000000L;

        System.out.println(iniShort);


        // Floating Point Number
        float iniFloat = 10.12f;
        double iniDouble = 12.2424;

        System.out.println(iniFloat);

        // Literals Number
        int decimalInt = 34; // ditulis seperti biasa
        int hexaDecimalInt = 0x145FF; // ditambah 0x di depan angka hexa
        int binaryInt = 0b1010111; // ditambah 0b di depan angka biner

        System.out.println(hexaDecimalInt);

        // Underscore (hanya sebagai bantuan pemisah angka)
        int amount1 = 1000000000; // lebih sulit dilihat mata
        int amount2 = 1_000_000_000; // lebih mudah dilihat mata




    }






}
